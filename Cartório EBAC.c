int consulta() //função responsável por procurar o usuário do sistema
{
	setlocale(LC_ALL, "Portuguese"); //definindo linguagem
	
	//incio da criação de variáveis/string
	char cpf[40];
	char conteudo[200];
	//fim da criação de variáveis/string
	
	printf("Digite o CPF a ser consultado: "); //colentando informação do usuário
	scanf("%s",cpf); //%s refere-se a string
	
	FILE *file;
	file = fopen(cpf, "r"); //abre o arquivo e "r" significa ler
	
	if( file == NULL)
	{
		printf("Não foi possível abrir o arquivo, não localizado!\n");
	}
	
	while(fgets(conteudo, 200, file) != NULL)
	{
		printf("\nEssas são as informações do usuário: ");
		printf("%s", conteudo);
		printf("\n\n");
	}
	
	system("pause"); //responsável por pausar a tela
	fclose(file); //fecha o arquivo
}

